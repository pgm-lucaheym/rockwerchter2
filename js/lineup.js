  function convertSocial(social){
      socStr = '';
      social.forEach(function (soc, index){
          socStr += `<li><a href="#">${soc.website}</a></li>`
          socStr += `<li><a href="#">${soc.facebook}</a></li>`
          socStr += `<li><a href="#">${soc.twitter}</a></li>`
          socStr += `<li><a href="#">${soc.instagram}</a></li>`
      })
      return socStr;
  }
  function covertArtist(artist){
      artStr = '';
      artist.forEach(function (art, index){
          artStr += `<p>${art.synopsis}</p>`
          artStr += `${convertMedia(art.media)}`
          artStr += `<ul>${convertSocial(art.social)}</ul>`
      })
      return artStr;
  }
